//// LIBS /////////////////////////////////////////////////////////////////////

const { Flux }  = require('delorean');
var   fileStore = require('../store/file-store');

let Dispatcher = Flux.createDispatcher({

  removeItem(payload) {

    this.dispatch('removeItem', payload);

  },

  progressUpload(item) {

    this.dispatch('progressUpload', item);

  },

  getStores() {

    return {
      fileStore : fileStore
    };

  }

});

///// EXPORTS /////////////////////////////////////////////////////////////////

module.exports = Dispatcher;

